class Contact:
    def __init__(self,
                 firstname,
                 middlename,
                 address,
                 email,
                 homepage,
                 bday,
                 bmonth,
                 byear):
        self.firstname = firstname
        self.middlename = middlename
        self.address = address
        self.email = email
        self.homepage = homepage
        self.bday = bday
        self.bmonth = bmonth
        self.byear = byear

